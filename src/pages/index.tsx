interface TimerState {
	pomodoro: number;
	short: number;
	long: number;
	changePomodoro: (newValue: number) => void;
	changeShort: (newValue: number) => void;
	changeLong: (newValue: number) => void;
}

// eslint-disable-next-line @typescript-eslint/no-unused-vars
export const useTimerStore = create<TimerState>()((set, get) => ({
	pomodoro: 25,
	short: 5,
	long: 10,
	changePomodoro: (newValue) => {
		set({ pomodoro: newValue });
	},

	changeShort: (newValue) => {
		set({ short: newValue });
	},

	changeLong: (newValue) => {
		set({ long: newValue });
	},
}));

const pomodoro = useTimerStore.getState().pomodoro;
